                <h2>Mengapa Yoga Penting bagi Lansia?</h2>
                <p>
                    Seiring bertambahnya usia, tubuh mengalami berbagai perubahan yang bisa memengaruhi mobilitas dan kesehatan secara keseluruhan. Keseimbangan, kekuatan otot, dan fleksibilitas mulai menurun, membuat lansia lebih rentan terhadap cedera, seperti jatuh. Di sinilah yoga memainkan peran penting. Dengan gerakan lembut dan fokus pada pernapasan, yoga bisa menjadi cara efektif untuk mengurangi risiko cedera, meningkatkan keseimbangan, dan meningkatkan kesehatan mental.
                </p>

                <h2>Manfaat Yoga bagi Lansia</h2>
                <p>
                    Meningkatkan Fleksibilitas: Seiring bertambahnya usia, fleksibilitas tubuh biasanya berkurang, terutama di area punggung dan kaki. Yoga membantu meningkatkan fleksibilitas otot dan sendi melalui gerakan yang dirancang khusus untuk lansia. Fleksibilitas ini dapat meningkatkan kemampuan lansia dalam melakukan aktivitas sehari-hari dengan lebih mudah dan tanpa rasa sakit.
                </p>
                <p>
                    Meningkatkan Keseimbangan dan Mencegah Cedera: Salah satu masalah utama yang sering dialami lansia adalah risiko jatuh akibat hilangnya keseimbangan. Pose-pose yoga seperti Tree Pose atau Warrior Pose membantu melatih keseimbangan, yang penting untuk mencegah jatuh dan cedera.
                </p>
